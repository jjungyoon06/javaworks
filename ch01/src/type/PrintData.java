package type;

public class PrintData {

	public static void main(String[] args) {
		//숫자
		System.out.println(100);
		System.out.println(3.3);
		System.out.println(100+10);
		System.out.println("======================");
		
		//문자
		System.out.println('A'); //홑 따옴표 : 문자 한 글자 표기
		System.out.println("B"); //쌍 따옴표 : 문자열포기
		System.out.println("banana"); //쌍 따옴표 : 문자열포기
		System.out.println(100+"10"); //문자 연결
		System.out.println("======================");
		
		//불리언
		System.out.println(true); 
		System.out.println(4 > 5); 
		
	}

}
